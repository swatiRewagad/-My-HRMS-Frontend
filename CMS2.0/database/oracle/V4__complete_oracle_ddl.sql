-- Core (from V1, repeated for standalone execution)
DECLARE
    v_count NUMBER;
BEGIN
    FOR seq_rec IN (
        SELECT column_value AS seq_name FROM TABLE(SYS.ODCIVARCHAR2LIST(
            'BANKS_SEQ', 'COMPLAINT_CATEGORIES_SEQ', 'COMPLAINTS_SEQ',
            'COMPLAINT_TIMELINE_SEQ', 'COMPLAINT_ATTACHMENTS_SEQ',
            'REGULATED_ENTITIES_SEQ', 'HOLIDAYS_SEQ', 'PINCODE_MASTER_SEQ',
            'MRE_RULE_CATEGORY_SEQ', 'MRE_RULE_DEFINITION_SEQ',
            'MRE_RULE_HISTORY_SEQ', 'MRE_DEPLOYMENT_SEQ',
            'FORM_CONFIG_SEQ', 'SUPPORTED_LOCALES_SEQ',
            'TRANSLATIONS_SEQ', 'TRANSLATION_KEYS_SEQ',
            'EXTRACTION_RULES_SEQ', 'REPORT_DEFINITIONS_SEQ',
            'RE_RESPONSE_TRACKER_SEQ', 'SIMULATED_EMAILS_SEQ',
            'EMAIL_DRAFTS_SEQ', 'CITIZEN_EMAIL_VERIFICATIONS_SEQ',
            'OTP_ATTEMPTS_SEQ', 'CAPTCHA_SESSIONS_SEQ',
            -- New sequences for V4
            'APPEALS_SEQ', 'APPEAL_TIMELINE_SEQ', 'AUDIT_LOG_SEQ',
            'COMMENT_TEMPLATES_SEQ', 'COMMUNICATION_TEMPLATES_SEQ',
            'EMAIL_DRAFT_ATTACHMENTS_SEQ', 'EMAIL_IGNORE_LIST_SEQ',
            'IN_APP_NOTIFICATIONS_SEQ', 'LOGIN_COOLOFFS_SEQ',
            'REPORT_SCHEDULES_SEQ', 'DEO_USER_SEQ',
            'EMAIL_ATTACHMENT_SEQ', 'ATTACHMENT_METADATA_SEQ',
            'COMPLAINT_MASTER_SEQ', 'COMPLAINT_HISTORY_SEQ',
            'OUTBOX_EVENT_SEQ', 'ELIGIBILITY_AUDIT_SEQ',
            'QUESTION_MASTER_SEQ', 'RULE_CATEGORY_SEQ',
            'RULE_DEFINITION_SEQ', 'RULE_DEPLOYMENT_LOG_SEQ',
            'RULE_VERSION_HISTORY_SEQ', 'PINCODES_SEQ',
            'EXTRACTION_RULE_SEQ'
        ))
    ) LOOP
        SELECT COUNT(*) INTO v_count FROM USER_SEQUENCES WHERE SEQUENCE_NAME = seq_rec.seq_name;
        IF v_count = 0 THEN
            EXECUTE IMMEDIATE 'CREATE SEQUENCE ' || seq_rec.seq_name || ' START WITH 1 INCREMENT BY 1 NOCACHE';
        END IF;
    END LOOP;
END;
