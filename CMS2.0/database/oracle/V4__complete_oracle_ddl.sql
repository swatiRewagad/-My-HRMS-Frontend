-- Active complaints with SLA status
CREATE OR REPLACE VIEW VW_ACTIVE_COMPLAINTS AS
SELECT
    c.ID,
    c.COMPLAINT_NUMBER,
    c.COMPLAINANT_NAME,
    c.COMPLAINANT_EMAIL,
    c.STATUS,
    c.PRIORITY,
    c.DEPARTMENT,
    c.ASSIGNED_OFFICER,
    c.ASSIGNED_ROLE,
    c.WORKFLOW_STAGE,
    c.SLA_DEADLINE,
    c.CREATED_AT,
    c.FILED_AT,
    CASE
        WHEN c.SLA_DEADLINE IS NULL THEN 'NO_SLA'
        WHEN SYSTIMESTAMP > c.SLA_DEADLINE THEN 'BREACHED'
        WHEN SYSTIMESTAMP > c.SLA_DEADLINE - INTERVAL '2' DAY THEN 'AT_RISK'
        ELSE 'ON_TRACK'
    END AS SLA_STATUS
FROM COMPLAINTS c
WHERE c.STATUS NOT IN ('resolved', 'closed', 'rejected', 'withdrawn');

-- Outbox events pending publishing
CREATE OR REPLACE VIEW VW_PENDING_OUTBOX AS
SELECT
    EVENT_ID,
    AGGREGATE_TYPE,
    AGGREGATE_ID,
    EVENT_TYPE,
    TOPIC,
    STATUS,
    RETRY_COUNT,
    CREATED_AT,
    ROUND((CAST(SYSTIMESTAMP AS DATE) - CAST(CREATED_AT AS DATE)) * 24 * 60, 1) AS MINUTES_PENDING
FROM OUTBOX_EVENT
WHERE STATUS IN ('PENDING', 'FAILED')
ORDER BY CREATED_AT ASC;

