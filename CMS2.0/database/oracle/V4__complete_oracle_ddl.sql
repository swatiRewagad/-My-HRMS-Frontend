-- Procedure: Generate next complaint number
CREATE OR REPLACE PROCEDURE SP_GENERATE_COMPLAINT_NUMBER(
    p_city_code    IN  VARCHAR2,
    p_complaint_no OUT VARCHAR2
) AS
    v_year   VARCHAR2(4);
    v_seq    NUMBER;
BEGIN
    v_year := TO_CHAR(SYSDATE, 'YYYY');
    SELECT NVL(MAX(TO_NUMBER(SUBSTR(COMPLAINT_NUMBER, -6))), 0) + 1
    INTO v_seq
    FROM COMPLAINTS
    WHERE COMPLAINT_NUMBER LIKE 'CMS/' || v_year || '/' || p_city_code || '/%';

    p_complaint_no := 'CMS/' || v_year || '/' || p_city_code || '/' || LPAD(v_seq, 6, '0');
END SP_GENERATE_COMPLAINT_NUMBER;
/

-- Procedure: Publish outbox events (mark as PUBLISHED)
CREATE OR REPLACE PROCEDURE SP_MARK_OUTBOX_PUBLISHED(
    p_event_id IN NUMBER
) AS
BEGIN
    UPDATE OUTBOX_EVENT
    SET STATUS = 'PUBLISHED',
        PUBLISHED_AT = SYSTIMESTAMP
    WHERE EVENT_ID = p_event_id
      AND STATUS IN ('PENDING', 'FAILED');

    COMMIT;
END SP_MARK_OUTBOX_PUBLISHED;
/

-- Procedure: Mark breached RE response trackers
CREATE OR REPLACE PROCEDURE SP_CHECK_RE_WINDOW_BREACH AS
BEGIN
    UPDATE RE_RESPONSE_TRACKER
    SET BREACHED = 1,
        EX_PARTE_ELIGIBLE = 1,
        UPDATED_AT = SYSTIMESTAMP
    WHERE BREACHED = 0
      AND RESPONDED_AT IS NULL
      AND WINDOW_EXPIRES_AT < SYSTIMESTAMP;

    COMMIT;
END SP_CHECK_RE_WINDOW_BREACH;
/

-- Procedure: Cleanup expired OTPs and captchas
CREATE OR REPLACE PROCEDURE SP_CLEANUP_EXPIRED_TOKENS AS
BEGIN
    DELETE FROM OTP_ATTEMPTS WHERE EXPIRES_AT < SYSTIMESTAMP - INTERVAL '1' DAY;
    DELETE FROM CAPTCHA_SESSIONS WHERE EXPIRES_AT < SYSTIMESTAMP - INTERVAL '1' DAY;
    DELETE FROM CITIZEN_EMAIL_VERIFICATIONS WHERE EXPIRES_AT < SYSTIMESTAMP - INTERVAL '7' DAY AND VERIFIED = 0;
    DELETE FROM LOGIN_COOLOFFS WHERE COOLOFF_UNTIL < SYSTIMESTAMP - INTERVAL '7' DAY;
    COMMIT;
END SP_CLEANUP_EXPIRED_TOKENS;
/

